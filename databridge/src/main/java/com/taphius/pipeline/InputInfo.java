package com.taphius.pipeline;

public class InputInfo {
	String inputPath;
	String inputSchema;
	String inputDataSet;
	/**
	 * @return the inputPath
	 */
	public String getInputPath() {
		return inputPath;
	}
	/**
	 * @param inputPath the inputPath to set
	 */
	public void setInputPath(String inputPath) {
		this.inputPath = inputPath;
	}
	/**
	 * @return the inputSchema
	 */
	public String getInputSchema() {
		return inputSchema;
	}
	/**
	 * @param inputSchema the inputSchema to set
	 */
	public void setInputSchema(String inputSchema) {
		this.inputSchema = inputSchema;
	}
	/**
	 * @return the inputDataSet
	 */
	public String getInputDataSet() {
		return inputDataSet;
	}
	/**
	 * @param inputDataSet the inputDataSet to set
	 */
	public void setInputDataSet(String inputDataSet) {
		this.inputDataSet = inputDataSet;
	}
}
